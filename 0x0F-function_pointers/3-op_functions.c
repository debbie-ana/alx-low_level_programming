#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result as integer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtractss two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result as integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result as integer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result as integer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder of division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: result as integer
 */

int op_mod(int a, int b)
{
	return (a % b);
}
